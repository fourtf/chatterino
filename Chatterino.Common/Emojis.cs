        public static string ReplaceShortCodes(string s)
        {
            return findShortCodes.Replace(s, m =>
            {
                string emoji;
                if (ShortCodeToEmoji.TryGetValue(m.Groups[1].Value, out emoji))
                    return emoji;
                return m.Value;
            });
        }

